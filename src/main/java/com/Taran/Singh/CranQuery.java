package com.Taran.Singh;

public class CranQuery {


    private String id;
    private String abstr;

    public CranQuery(String id, String abstr) {
        this.id = id;
        this.abstr = abstr;
    }


    public String getID() {
        return id;
    }


    public String getAbstr() {
        return abstr;
    }


}